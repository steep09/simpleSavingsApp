import UIKit

extension UIView {
    
    func universalViewDesign() {
        self.backgroundColor = .white
        self.layer.shadowRadius = 5.0
        self.layer.shadowOpacity = 0.25
        self.layer.cornerRadius = 10.0
    }
    
}
